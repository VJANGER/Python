leng = ["Python", "Ruby", "PHP", "Javascript", "Java"]
leng[1] = "Go" # colocando el indice del objeto en la lista se puede modificar
print(leng)
print(leng[1]) # las listas en python el primer elemento de la lista el indice es 0
print(leng[-1]) # numeros positivos cuenta los indices de izquierda a derecha, numeros negativos cuenta de derech a izquierda
print(leng[-2]) # numeros positivos cuenta los indices de izquierda a derecha, numeros negativos cuenta de derech a izquierda
print(leng[1:3]) # (:) indica desde hasta donde (desde : hasta antes de)
print(leng[:3]) # si el punto de inicio esta vacio toma el primer elemento de la lista
print(leng[1:]) # si el punto de finalizacion esta vacio toma el ultimo elemnto de la lista
